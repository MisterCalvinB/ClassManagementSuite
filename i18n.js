  window.getLang = function () {
    try {
      var cfg = JSON.parse(localStorage.getItem('cmt-general-config') || '{}');
      return cfg.language || 'fr';
    } catch (e) { return 'fr'; }
  };

  window.t = function (key) {
    var lang = getLang();
    var map = STRINGS[lang] || STRINGS.en;
    if (map[key] !== undefined) return map[key];
    if (STRINGS.en[key] !== undefined) return STRINGS.en[key];
    return key;
  };

  // Replace {name} placeholder in a template string.
  window.tFmt = function (key, vars) {
    var s = t(key);
    if (vars) {
      Object.keys(vars).forEach(function (k) {
        s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
      });
    }
    return s;
  };

  window.applyTranslations = function () {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      el.title = t(el.getAttribute('data-i18n-title'));
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
    });
    // optgroup label attribute
    document.querySelectorAll('[data-i18n-label]').forEach(function (el) {
      el.setAttribute('label', t(el.getAttribute('data-i18n-label')));
    });
  };
